#escape sequence is used for multiple purposes 
# there are many escape sequences avaialbe in python
# \n is used for new line 
# \t is used for new Tab
# \b is used for backspace
print("Hello World")   #without escape sequence

print("Hello\nWorld")   #with \n  for new line

print("Hello\tWorld")   #with \t  for tab 

print("Hello\bWorld")   #with \b   for backspace

print("Hello\"World\"")   #with \""  for dobule quotes 